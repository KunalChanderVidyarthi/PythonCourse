#Atomic Data Types

#int variable
variableInt = 56
print(variableInt,type(variableInt))

#float variable
variableFloat = 0.6589
print(variableFloat, type(variableFloat))

#Boolean variable
variableBoolean = True
print(variableBoolean, type(variableBoolean))

# Compound data types

#string variable
variableStr = "Hello World"
print(variableStr, type(variableStr))

#list/array variable
variableList = [45,32.4,"kunal",True]
print(variableList, type(variableList))

#Dictionary variable
variableDict = {

               "grade1": ["joshin","adarsh","jason"],
               "grade2": ["dalceena","suresh"],
               "grade3": ["raj"]
        
        }

print(variableDict, type(variableDict))
